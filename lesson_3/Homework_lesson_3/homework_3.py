a = float(input("Введіть коефіцієнт a: "))
b = float(input("Введіть коефіцієнт b: "))
c = float(input("Введіть коефіцієнт c: "))

D = b**2 - 4*a*c

if D > 0:
    x1 = float((-b - D**0.5) / (2*a))
    x2 = float((-b + D**0.5) / (2*a))
    print("Корені рівняння: x1 =", x1, ';' "x2 =", x2)
elif D == 0:
    x = float(-b / (2 * a))
    print("Дискримінант = 0, тому x = ", x)
else:
    print("корені відсутні при від’ємному значенні дискримінанта!")

