from collections import namedtuple

# Створення іменованого кортежу для оцінок учнів
Grade = namedtuple('Grade', ['алгебра', 'геометрія', 'історія', 'інформатика', 'географія'])

# Фабрика оцінок для учнів
student1 = Grade(алгебра=95, геометрія=87, історія=92, інформатика=88, географія=90)
student2 = Grade(алгебра=88, геометрія=90, історія=85, інформатика=91, географія=89)

# Виведення даних на екран
print("Оцінки учня 1:")
print(student1)
print("\nОцінки учня 2:")
print(student2)
